

CREATE DATABASE `stockmarketing`; 



CREATE TABLE STOCK(STOCK_ID INT PRIMARY KEY,STOCK_NAME varchar(255), TICKER VARCHAR(100));
CREATE TABLE TRADER(TRADER_ID INT PRIMARY KEY,TRADER_NAME varchar(255), EMAIL VARCHAR(255),PHONE VARCHAR(150));
CREATE TABLE TRADE(TRADE_ID INT PRIMARY KEY,STOCK_ID INT,TRADER_ID INT ,QUANTITY INT,PRICE DECIMAL(10,2),TIME_STAMP TIMESTAMP,
FOREIGN KEY(STOCK_ID)REFERENCES STOCK(STOCK_ID),FOREIGN KEY(TRADER_ID)REFERENCES TRADER(TRADER_ID) );
